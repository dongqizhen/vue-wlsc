          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 本条款说明：
          产品性质不适宜退货的情形，包括但不限于：<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ①已过期或已拆封的特殊产品。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ②已损毁的产品。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   (二).定制类产品相关注意事项<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          1、针对定制类产品交易，买家应在订单成交前与卖家就定制产品的样品或产品细节进行交互确认，否则因此导致的损失由买家自行承担。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          2、若买家主张定制产品与样品不一致，或细节与约定不符，须提交存在差异的相关事实材料，否则买家应承担不利后果。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          3、因买家原因导致样品毁损或丢失而无法对样品与定制产品间的一致性进行确认的，相应责任由买家自行承担。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          4、针对定制类产品交易，如买卖双方约定定金方式交易，适用定金罚则。买家不履行合同义务的，无权请求返还定金；卖家不履行合同义务的，双倍返还定金。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  
          (三).杂款库存产品相关注意事项<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          1、杂款库存产品一般是指随着季节变换、时间推移，因为拼单而余下的产品，通常规格/颜色不全、包装不统一。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          2、卖家实际发货的产品必须包含在产品描述中，或事先与买家进行过明确说明约定，否则当买家实际收到产品不一致的情况，卖家应承担不利后果。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   (四).价格优惠及赠品问题<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          1、针对存在价格优惠的，任意方不得基于交易后期发生部分或全部退货的情况，主张按照优惠前的产品价格计算相关费用，但双方另有约定的除外。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          2、针对卖家未在销售页面进行标注或在订单成交前未告知买家其所购产品附送赠品的，买家不得就赠品主张异议，无论最终买家是否收到赠品。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          3、卖家须在销售页面显著位置标注赠品价值，否则不得在后续发生赠品退回且损坏的情况下，向买家主张损失赔偿。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   (五).其他注意事项 <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          1、卖家对销售的产品负有承担售后问题的责任，须自觉遵守对买家做出的售后服务承诺，并遵守网来商城的规则及相关法律、法规的规定。二手、另有约定的除外。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          2、买家不得以部分产品存在描述不符或质量问题为由，主张全部产品的退款或退（换）货，如根据产品特性确需全部退货或交易双方另有约定的除外。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          3、卖家发出的产品被国家行政司法机关（包括但不限于海关、工商行政管理局、人民法院等）扣押、查封，在买家提供相关证明材料后，卖家应承担相应责任。
        </p>
