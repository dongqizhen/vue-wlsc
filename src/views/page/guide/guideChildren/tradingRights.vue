          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          4、交易争议处理：指买卖双方在交易履行过程中产生交易争议，因双方无法自行协商或协商不能达成一致，一方或双方申请提交网来商城进行交易调处处理。
