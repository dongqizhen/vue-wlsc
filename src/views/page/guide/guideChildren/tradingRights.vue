          2、投诉方：指在网来商城平台上发起交易争议处理诉求的用户。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          3、被投诉方：指投诉方投诉的网来商城平台用户。
