          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          ③跨境转运的，产品被收件人/买家委托的境内承运人签收的；
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          ④收件人信息缺失或不特定，产品在收件地址被签收的；
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ⑤产品在卖家退货地址被签收的；
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          ⑥其他依据交易习惯及常人认知水准综合判断后予以认定的情形。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3、退货<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （1）交易双方达成退货协议后，卖家有义务向买家提供有效的收货地址，保证本人或相关授权代收人可签收，否则因此导致的损失由卖家承担。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （2）若交易双方达成（含线下达成）退货/换货/维修协议时卖家已提供提货地址且双方无运费争议，买家应在与卖家约定时间内将产品寄出，逾期卖家有权拒绝继续退货，但因物流原因耽误退货或双方特殊情况另有约定的除外。 若卖家同意退货/换货/维修协议但未提供退货地址或者双方有运费争议未协商，平台介入后将重新处理，如为卖家责任支持退货退款运费卖家承担；如为买家责任，无法支持退货。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （3）买家不得擅自使用平邮、运费到付的方式进行退货，使用物流方式退货时，应提前与卖家确认是否方便提货，否则导致损失由买家承担。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （4）在产品本身无问题，交易双方协议退货的情况下，买家须保证所退产品不影响卖家的二次销售，否则买家需承担全部责任。<br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
