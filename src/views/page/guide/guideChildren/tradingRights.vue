        <p>
          六、买卖家交易须知
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (一).发货、收货、退货相关问题
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1、发货
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （1）
          买家下单时，有义务向卖家提供有效的收货地址，保证本人或相关授权代收人可签收，如临时变更地址，需及时与卖家沟通并达成一致，否则因此导致的损失由买家承担。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （2）卖家发布产品信息时应当对产品承运、配送方式（自提、物流配送等）及发货运费担负等相关要素作出清晰、显著的表述或与买家约定一致。否则，由此引发的争议由卖家承担相关不利后果。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （3）买家对发货时限、到货时限、包装方式等有特殊要求，应在下单前与卖家确认，或在下单后取得卖家同意，否则之后拒收或主张退货，相应风险由买家自行承担。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （4）已约定发货或到货时间，因卖家原因超期，买家拒收后，卖家应自行联系物流及时召回产品，否则因此导致的损失由卖家自行承担。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2、收货
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （1）买家签收产品前，应就能够即时验收的表面一致问题，包括但不限于是否存在破损、变形、少/错件等情况进行判断，并有权在出现上述情形时拒签产品；一旦签收，则不得以产品存在即时验收范围内的瑕疵或缺陷为由主张退款、退（换）货，但买家能够提供有效相反证据或交易双方另有约定的除外。卖家收款后遵循同样规则。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （2）买家无正当理由拒签产品，产生的损失由买家承担。卖家收退货时遵循同样规则。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （3）卖家违反发货规范但买家点击“确认收货”的，买家以卖家违反发货规范为由而主张退货退款的，应当和卖家协商一致，否则将驳回买家维权诉求。因卖家的不正当行为诱使买家“确认收货”的情形除外。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （4）卖家违反发货规范但买家拒绝退货的，交易做打款卖家处理。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          （5）卖家发货或买家退货后，收件人应当亲自签收产品，也可委托他人签收产品或指示承运人将产品置于收件人指定的地点。收件人委托他人签收产品或承运人已按收件人的指示将产品置于指定地点的，视为收件人本人签收。
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 本条款说明：
          符合下述任一情形的，亦将视为收件人本人签收：
