          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ①收件人成年家属签收的；
          <br />
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          ②经收件人同意，置于智能快递柜的产品；
